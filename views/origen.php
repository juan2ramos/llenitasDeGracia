    <h2> Y para desayunar hoy <span class="signo">¿</span>arepa o pan<span class="signo">?</span></h2>

    <p>Uno de los beneficios más importantes de la arepa, es que contiene menos gluten que el pan (harina de trigo),
        por
        lo que las personas que padecen intolerancia al gluten o alergia a la misma, encuentran en la arepa una opción
        más
        saludable. También tiene un buen contenido de tiamina, vitamina A, vitamina E.
    </p>

    <h2> Datos curiosos de la AREPA</h2>
    <ul>
        <li>
            En Colombia especialmente, la arepa hace parte del patrimonio cultural y es considerada como un símbolo de
            unidad
            gastronómica nacional. En ese país existen 75 tipos de variedades de arepa.
        </li>
        <li>
            La preparación de las arepas es sencilla, basta con mezclar harina de maíz, sal y agua, se les da la forma
            redonda
            y se asan en planchas y parrillas de hierro, utensilios que permiten su fácil preparación en hogares y
            fábricas.
        </li>
        <li>
            Dentro de sus múltiples modalidades están las "telitas" de la región andina, las cuales se llaman así debido
            a
            que
            son delgadas. En el Centro y Oriente, se preparan más gruesas y tostadas. Mientras que en Occidente, son más
            bien
            "peladas" porque se preparan con cenizas.
        </li>
        <li>
            Se suelen comer rellenas con queso y mantequilla, carne mechada o asado, con jamón o con casi cualquier cosa
            que
            se le pueda ocurrir.
        </li>
        <li>
            Nutricionalmente aportan todos los beneficios de la harina de maíz, entre los cuales destacan los
            carbohidratos y
            las proteínas.
        </li>
    </ul>
