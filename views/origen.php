    <h2> TIPIS</h2>

    <p>Llenitas de Gracia te cuenta por qué es mejor consumir arepa</p>

    <ol>
        <li>
            La arepa está hecha de maíz, un alimento que gracias a sus virtudes energéticas, su consumo resulta
            especialmente
            interesante para todas aquellas personas que practican deporte, y que a fin de cuentas necesitan que les
            proporcionen mucha energía, la cual sea digerida rápido.
        </li>
        <li>
            El maíz es el único cereal en el que encontramos betacaroteno, a la par que aporta fibras, hidratos de
            carbono, y
            una interesantísima cantidad de vitaminas del grupo B (en particular B1 y B3).
        </li>
        <li>
            El maíz ayuda a metabolizar las grasas de manera mucho más rápida, a la par que mejora el tránsito
            intestinal
            (ideal en casos de estreñimiento), y reduciendo el colesterol alto.
        </li>
    </ol>
